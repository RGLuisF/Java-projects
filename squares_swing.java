    public void segmento(double x1, double y1, double x2, double y2){
        jPanel1.getGraphics().drawLine((int)(mx+escX*x1), (int)(mx-escY*y1), (int)(mx+escX*x2), (int)(mx-escY*y2));
    }
    
    public void coordenada(double x, double y){
        jPanel1.getGraphics().drawOval((int)(mx+escX*x), (int)(mx-escY*y), 2, 2);
    }
    
     public double Pitagoras(double x1, double y1, double x2, double y2){
        double d;
        d = Math.sqrt((x2-x1)*(x2-x1)+(y2-y1)*(y2-y1));
        return d;
    }

