    private void DibujaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DibujaActionPerformed
     double x1,y1,x2,y2,x3,y3,x4,y4,p1,p2,p3,p4;
        try{
           x1=Double.parseDouble(this.x1.getText());
           y1=Double.parseDouble(this.y1.getText());
           x2=Double.parseDouble(this.x2.getText());
           y2=Double.parseDouble(this.y2.getText());
           x3=Double.parseDouble(this.x3.getText());
           y3=Double.parseDouble(this.y3.getText());
           x4=Double.parseDouble(this.x4.getText());
           y4=Double.parseDouble(this.y4.getText());
       }
       catch(RuntimeException ex){
           JOptionPane.showMessageDialog(this,
                   "ERROR CON LAS COORDENADAS DADAS",
                   "ERROR", JOptionPane.ERROR_MESSAGE);
           return;
       }
        segmento(x1,y1,x2,y2);
        segmento(x1,y1,x3,y3);
        segmento(x1,y1,x4,y4);
        segmento(x2,y2,x3,y3);
        segmento(x2,y2,x4,y4);
        segmento(x3,y3,x4,y4);
        
        p1=Pitagoras(x1,y1,x2,y2)+Pitagoras(x1,y1,x3,y3)+Pitagoras(x1,y1,x4,y4);
        p2=Pitagoras(x2,y2,x1,y1)+Pitagoras(x2,y2,x3,y3)+Pitagoras(x2,y2,x4,y4);
        p3=Pitagoras(x3,y3,x1,y1)+Pitagoras(x3,y3,x2,y2)+Pitagoras(x3,y3,x4,y4);
        p4=Pitagoras(x4,y4,x1,y1)+Pitagoras(x4,y4,x2,y2)+Pitagoras(x4,y4,x3,y3);
        
        jPanel1.getGraphics().drawString("P1="+p1,20, 20);
        jPanel1.getGraphics().drawString("P2="+p2,20, 35);
        jPanel1.getGraphics().drawString("P3="+p3,20, 50);
        jPanel1.getGraphics().drawString("P4="+p4,20, 65);
    }//GEN-LAST:event_DibujaActionPerformed

